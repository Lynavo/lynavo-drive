const NEUTRAL_ELECTRON_BUILDER_CONFIG = 'electron-builder.yml';

const TARGETS = new Set(['ios', 'android', 'mac', 'win', 'linux']);

const RELEASE_PROFILES = Object.freeze({
  prod: Object.freeze({
    name: 'prod',
    channel: 'prod',
    review: false,
    electronBuilderConfig: NEUTRAL_ELECTRON_BUILDER_CONFIG,
  }),
  review: Object.freeze({
    name: 'review',
    channel: 'review',
    review: true,
    electronBuilderConfig: NEUTRAL_ELECTRON_BUILDER_CONFIG,
  }),
});

export function listReleaseProfileNames() {
  return Object.keys(RELEASE_PROFILES);
}

export function getReleaseProfile(name) {
  const profile = RELEASE_PROFILES[name];
  if (!profile) {
    throw new Error(
      `Unsupported release profile "${name}". Supported profiles: ${listReleaseProfileNames().join(', ')}`,
    );
  }
  return profile;
}

export function parseTargets(value = 'ios,mac,win') {
  const targets = value
    .split(',')
    .map((target) => target.trim())
    .filter(Boolean);

  if (targets.length === 0) {
    throw new Error('At least one release target is required.');
  }

  for (const target of targets) {
    if (!TARGETS.has(target)) {
      throw new Error(
        `Unsupported release target "${target}". Supported targets: ios, android, mac, win, linux`,
      );
    }
  }

  return [...new Set(targets)];
}

export function buildReleasePlan({ profileName, targets }) {
  const profile = getReleaseProfile(profileName);
  assertProfileIntegrity(profile);

  const env = buildProfileEnv(profile);
  const steps = targets.map((target) => buildTargetStep(profile, target));

  return {
    profile,
    env,
    steps,
    mobileReleaseProfileSource: buildMobileReleaseProfileSource(profile),
  };
}

function assertProfileIntegrity(profile) {
  if (profile.review && profile.channel !== 'review') {
    throw new Error(`${profile.name} must use review channel.`);
  }
}

function buildProfileEnv(profile) {
  return {
    LYNAVO_RELEASE_CHANNEL: profile.channel,
    ELECTRON_BUILDER_CONFIG: profile.electronBuilderConfig,
  };
}

function buildTargetStep(profile, target) {
  if (target === 'ios') {
    return {
      target,
      command: 'pnpm',
      args: ['--filter', '@lynavo-drive/mobile', 'build:ios:release'],
    };
  }
  if (target === 'android') {
    return {
      target,
      command: 'bash',
      args: [
        '-lc',
        'cd apps/mobile/android && ./gradlew assembleRelease bundleRelease -PreactNativeArchitectures=arm64-v8a,x86_64',
      ],
    };
  }
  if (target === 'mac') {
    return {
      target,
      command: 'pnpm',
      args: ['package:desktop'],
    };
  }
  if (target === 'linux') {
    return {
      target,
      command: 'pnpm',
      args: ['--filter', '@lynavo-drive/desktop', 'package:linux'],
    };
  }
  return {
    target,
    command: 'pnpm',
    args: ['--filter', '@lynavo-drive/desktop', 'package:win'],
  };
}

function buildMobileReleaseProfileSource(profile) {
  return `// Generated by scripts/release/release.mjs. Do not edit by hand.
export const mobileReleaseProfile = {
  name: '${profile.name}',
  channel: '${profile.channel}',
  review: ${profile.review},
} as const;
`;
}
